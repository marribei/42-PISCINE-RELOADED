int	ft_count_if(char **tab, int (*f)(char*))
{
	int	i;
	int	j;

	if (!tab || !f)
		return (0);
	i = 0;
	j = 0;
	while (tab[i])
	{
		if ((*f)(tab[i]) == 1)
			j++;
		i++;
	}
	return (j);
}
